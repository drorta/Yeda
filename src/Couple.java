public class Couple {

    private int num;
    private int appears;

    public Couple(int num, int appears) {
        this.num = num;
        this.appears = appears;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getAppears() {
        return appears;
    }

    public void setAppears(int appears) {
        this.appears = appears;
    }
}
